# Week 1: Triangle Area

# Get the base and height using input statements
# Remember to use int() to convert the string to an integer
base = int( input("Enter the base: ") )
height = int( input("Enter the height: ") )

# Calculate the area
# Recall that * is multiplication and / is division
area = base * height / 2

# Print out the area of the triangle
print("The triangle has an area of", area)
